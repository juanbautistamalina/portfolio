// Lazy loading para imágenes
const imageObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const img = entry.target;
            img.src = img.dataset.src;
            img.classList.remove('lazy');
            imageObserver.unobserve(img);
        }
    });
});

// Aplicar lazy loading a imágenes
document.addEventListener('DOMContentLoaded', () => {
    const lazyImages = document.querySelectorAll('img[data-src]');
    lazyImages.forEach(img => imageObserver.observe(img));
});

// Filtro de proyectos (funcionalidad adicional)
function filterProjects(category) {
    const projects = document.querySelectorAll('.project-card');
    
    projects.forEach(project => {
        const techTags = project.querySelectorAll('.tech-tag');
        let hasCategory = false;
        
        techTags.forEach(tag => {
            if (tag.textContent.toLowerCase().includes(category.toLowerCase())) {
                hasCategory = true;
            }
        });
        
        if (category === 'all' || hasCategory) {
            project.style.display = 'block';
            project.style.animation = 'fadeIn 0.5s ease';
        } else {
            project.style.display = 'none';
        }
    });
}

